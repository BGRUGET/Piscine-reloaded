void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int ac, char **av)
{
	int i;

	i = 1;
	if (ac > 1)
	{
		while (i <= ac - 1)
		{
			ft_putstr(av[i]);
			i++;
			ft_putchar('\n');
		}
	}
	return (0);
}
